def elevar(base,exponente):
    """Qué hace la función?-> Elevar un número (base) a otro número (exponente)

    Args:
        base (int): número base de la potencia
        exponente (int): número exponente de la potencia

    Returns:
        int: resultado de la potencia, elevar base a exponente
    """
    return base**exponente

print(elevar(2,4))